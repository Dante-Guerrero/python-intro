# Tema: Calculo simple de multa
# Objetivo: Multiplicar una cantidad base por dias de retraso.
# Como ejecutarlo: python 06-ejercicios-derecho/01-calculo-multa-simple.py

# Este ejemplo usa una multa fija por cada dia de retraso.
multa_por_dia = 25
dias_retraso = 4
monto_total = multa_por_dia * dias_retraso

print("Multa por dia:", multa_por_dia)
print("Dias de retraso:", dias_retraso)
print("Monto total:", monto_total)
