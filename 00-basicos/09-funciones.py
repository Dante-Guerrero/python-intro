# Definimos una funcion que recibe dos numeros y devuelve la suma.
def sumar(primer_numero, segundo_numero):
    resultado = primer_numero + segundo_numero
    return resultado

# Llamamos a la funcion y mostramos el resultado.
print(sumar(3, 4))
