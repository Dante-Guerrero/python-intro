# Nombre: 01-variables.py
# Tema: Variables y tipos de datos
# Objetivo: Guardar distintos valores en variables y observar su tipo.
# Como ejecutarlo: python 00-basicos/01-variables.py

# Una variable guarda un dato para poder usarlo despues.
nombre_estudiante = "Manu"
edad = 36
promedio = 1.75
esta_matriculado = True

# type() permite ver de que tipo es cada dato.
print("Tipo de nombre_estudiante:", type(nombre_estudiante))
print("Tipo de edad:", type(edad))
print("Tipo de promedio:", type(promedio))
print("Tipo de esta_matriculado:", type(esta_matriculado))
