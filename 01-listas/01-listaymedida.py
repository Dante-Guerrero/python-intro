# Una lista puede guardar varios valores en orden.
expedientes = ["EXP-001", "EXP-002", "EXP-003", "EXP-004", "EXP-005"]

# len() devuelve la cantidad de elementos de la lista.
print("Cantidad de expedientes:", len(expedientes))
