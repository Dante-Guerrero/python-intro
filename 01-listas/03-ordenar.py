# Nombre: 03-ordenar.py
# Tema: Ordenar listas
# Objetivo: Ordenar una lista de palabras con sorted().
# Como ejecutarlo: python 01-listas/03-ordenar.py

# Creamos una lista con varias palabras.
palabras = ["demanda", "audiencia", "prueba", "apelacion", "contrato"]
print("Lista original:", palabras)

# sorted() devuelve una nueva lista ordenada.
palabras_ordenadas = sorted(palabras)
print("Lista ordenada:", palabras_ordenadas)
